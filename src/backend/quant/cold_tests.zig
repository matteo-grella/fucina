// ggml-golden dequantize fixtures for the cold decode formats.
//
// The constants below were generated ONCE (2026-07-02, M1 Max, arm64) by a
// small C harness that feeds deterministic pseudo-random rows through ggml's
// reference quantizers (guaranteeing valid blocks: in-range grid indices and
// live sign/scale sub-fields, iq4_xs scales_h included) and prints both the
// encoded block bytes and dequantize_row_<fmt>'s f32 output (bit patterns, so
// this file is self-contained):
//
//   LL=refs/llama.cpp
//   cc -O2 -ffp-contract=off -o dump harness.c \
//      -L $LL/build-cpu/bin -lggml-base -Wl,-rpath,$LL/build-cpu/bin
//   ./dump > goldens.zig
//
// Inputs (C expressions; t = the GGML_TYPE_* enum value, i = flat index):
//   src[i]  = -2.0f + 4.0f*mix32(0x9E3779B9u ^ (t<<16) ^ i)/4294967295.0f
//   imat[i] =  0.5f + 0.5f*mix32(0xA5A5A5A5u ^ (t<<16) ^ i)/4294967295.0f
// where mix32 = murmur3 fmix32 (s^=s>>16; s*=0x85EBCA6B; s^=s>>13;
// s*=0xC2B2AE35; s^=s>>16). Each format quantizes 2 rows via ggml's
// quantize_<fmt>(src, dst, 2, n_per_row, imat) after iq2xs/iq3xs grid init;
// n_per_row = 256 for the QK_K formats, 64 for iq4_nl/mxfp4/nvfp4.
//
// Oracle robustness: the harness linked against (a) the as-shipped llama.cpp
// build-cpu dylib, (b) ggml-quants.c recompiled with -ffp-contract=off, and
// (c) ggml-quants.c recompiled with default FP contraction produced byte-
// identical block bytes AND f32 expansions, so the expectations are plain-C-
// semantics ggml output. The expansions are compared bit-exactly: the cold
// dequantizers are operation-for-operation ports, and the existing
// dot-vs-own-dequant cross-consistency tests above extend this parity pin to
// the table-dot kernels.

const DequantGolden = struct {
    bytes: []const u8,
    expected_bits: []const u32,
};

fn expectDequantMatchesGolden(comptime rhs_dtype: DType, comptime golden: DequantGolden) !void {
    const Block = dtype_mod.Storage(rhs_dtype);
    const block_count = golden.bytes.len / @sizeOf(Block);
    comptime std.debug.assert(block_count * @sizeOf(Block) == golden.bytes.len);
    comptime std.debug.assert(block_count * dtype_mod.blockSize(rhs_dtype) == golden.expected_bits.len);

    // The 2 quantized source rows dequantize identically as one concatenated
    // row: every cold decoder is per-block stateless.
    var blocks: [block_count]Block = undefined;
    @memcpy(std.mem.sliceAsBytes(blocks[0..]), golden.bytes);
    var out: [golden.expected_bits.len]f32 = undefined;
    try dequantizeRowForDType(rhs_dtype, &out, &blocks);

    var out_bits: [golden.expected_bits.len]u32 = undefined;
    for (out, &out_bits) |value, *bits| bits.* = @bitCast(value);
    try std.testing.expectEqualSlices(u32, golden.expected_bits, &out_bits);
}

