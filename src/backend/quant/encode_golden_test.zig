test "legacy encoders: degenerate-but-finite blocks are defined (no @intFromFloat UB)" {
    // Two degenerate-but-FINITE single blocks (32 elems):
    //  - subnormal spread: d is subnormal, 1/d overflows to inf, and
    //    0 * inf = NaN reaches the int conversion without the inv_d guard;
    //  - near +/-3.4e38 spread (q4_1/q5_1): max - min overflows to inf, so
    //    d = inf, inv_d = 0, and (x - min) * 0 = inf * 0 = NaN without the
    //    NaN-collapsing clamp.
    // The assertion is stability: encoding twice yields identical bytes and
    // never panics in Debug (ReleaseFast UB was empirically observed before
    // the guards landed).
    var subnormal = [_]f32{0} ** 32;
    subnormal[1] = 1e-40;
    subnormal[7] = -1e-41;
    var huge = [_]f32{3.4e38} ** 32;
    huge[0] = -3.4e38;
    huge[5] = 0;

    inline for (.{ subnormal, huge }) |x| {
        var q40_a: [1]qm.BlockQ4_0 = undefined;
