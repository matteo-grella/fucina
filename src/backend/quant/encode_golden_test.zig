const qk_k_block_size = qm.qk_k_block_size;

fn inputVector(index: usize) [qk_k_block_size]f32 {
    var out: [qk_k_block_size]f32 = undefined;
    for (&out, input_bits[index]) |*x, bits| x.* = @bitCast(bits);
    return out;
}

// Deterministic PRNG (same mix as the harness's input generator) for the
// fresh-data round-trip tests below.
fn mix32(seed: u32) u32 {
    var s = seed;
    s ^= s >> 16;
    s *%= 0x85EBCA6B;
    s ^= s >> 13;
    s *%= 0xC2B2AE35;
    s ^= s >> 16;
    return s;
}

fn fillUniform(out: []f32, seed: u32) void {
    for (out, 0..) |*x, i| {
        const bits = mix32(seed +% @as(u32, @intCast(i)) *% 0x9E3779B9);
        x.* = -1.0 + 2.0 * @as(f32, @floatFromInt(bits)) / 4294967295.0;
    }
}

fn rmse(a: []const f32, b: []const f32) f32 {
    var acc: f64 = 0;
    for (a, b) |x, y| {
        const d = @as(f64, x) - @as(f64, y);
        acc += d * d;
    }
    return @floatCast(@sqrt(acc / @as(f64, @floatFromInt(a.len))));
}

