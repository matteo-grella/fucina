//! (quantizeRow{Q4_K,Q5_K,Q6_K,Q4_1,Q5_0,Q5_1}Into and quantizeRowForDType).
//!
//! The constants below were generated ONCE (2026-06-11, M1 Max, arm64) by a
//! small C harness that extern-declares ggml's reference encoders
//! quantize_row_{q4_K,q5_K,q6_K,q4_1,q5_0,q5_1}_ref, runs them over the 8
//! input vectors below, and prints the inputs (f32 bit patterns, so this file
