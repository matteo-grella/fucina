/// Decodes one UTF-8 sequence at the head of `bytes`, writing the codepoint
/// to `cp` and returning the byte count (0 on malformed input). Mirrors
/// `duration_utf8_decode` (no overlong/surrogate rejection, truncated tails
/// are malformed).
pub fn utf8Decode(bytes: []const u8, cp: *u32) usize {
    if (bytes.len == 0) return 0;

    const c = bytes[0];
    if (c < 0x80) {
        cp.* = c;
        return 1;
    }
    if (c & 0xE0 == 0xC0 and bytes.len >= 2) {
        cp.* = (@as(u32, c & 0x1F) << 6) | @as(u32, bytes[1] & 0x3F);
        return 2;
    }
    if (c & 0xF0 == 0xE0 and bytes.len >= 3) {
        cp.* = (@as(u32, c & 0x0F) << 12) | (@as(u32, bytes[1] & 0x3F) << 6) | @as(u32, bytes[2] & 0x3F);
        return 3;
    }
    if (c & 0xF8 == 0xF0 and bytes.len >= 4) {
        cp.* = (@as(u32, c & 0x07) << 18) | (@as(u32, bytes[1] & 0x3F) << 12) |
            (@as(u32, bytes[2] & 0x3F) << 6) | @as(u32, bytes[3] & 0x3F);
        return 4;
    }
    return 0;
}

/// Binary search of the category table. Returns null if `cp` falls in a gap
/// (i.e. its Unicode category is one of L / C / O, which the estimator
/// resolves through the script ranges instead). Mirrors
/// `duration_cat_lookup`.
fn catLookup(cp: u32) ?Cat {
    var lo: isize = 0;
    var hi: isize = cat_ranges.len - 1;
    while (lo <= hi) {
        const mid = @divTrunc(lo + hi, 2);
        const r = cat_ranges[@intCast(mid)];
        if (cp < r.start) {
            hi = mid - 1;
        } else if (cp > r.end) {
            lo = mid + 1;
        } else {
            return r.cls;
        }
    }
    return null;
}

/// Weight for one codepoint, mirrors `RuleDurationEstimator._get_char_weight`
/// via `duration_get_char_weight`. The check order is normative: ASCII
/// letters, ASCII space, Arabic Tatweel, category table, script bisect,
/// upper planes, default.
pub fn getCharWeight(cp: u32) f32 {
    // ASCII letters take the latin fast path.
    if ((cp >= 'A' and cp <= 'Z') or (cp >= 'a' and cp <= 'z')) {
        return weightOf(.latin);
    }
    // ASCII space.
    if (cp == 0x20) {
        return weightOf(.space);
    }
    // Arabic Tatweel: explicit pre-empt before the category check.
    if (cp == 0x0640) {
        return weightOf(.mark);
    }

    // Unicode category check: M / P / S / Z / N short-circuit any
    // script-based weight.
    if (catLookup(cp)) |cat| {
        return switch (cat) {
            .m => weightOf(.mark),
            .p, .s => weightOf(.punctuation),
            .z => weightOf(.space),
            .n => weightOf(.digit),
        };
    }

    // bisect_left equivalent on script_ranges.end: finds the smallest
    // index i such that script_ranges[i].end >= cp.
    var lo: usize = 0;
    var hi: usize = script_ranges.len;
    while (lo < hi) {
        const mid = (lo + hi) / 2;
        if (script_ranges[mid].end < cp) {
            lo = mid + 1;
        } else {
            hi = mid;
        }
    }
    if (lo < script_ranges.len) {
        return weightOf(script_ranges[lo].script);
    }

    // Upper planes: CJK Ext B/C/D and historic scripts default to CJK.
    if (cp > 0x20000) {
        return weightOf(.cjk);
    }
    return weightOf(.default);
}

/// Sum of per-character weights over a UTF-8 string (f32 accumulation in
/// text order; a malformed byte skips 1). Mirrors `duration_total_weight`.
pub fn totalWeight(utf8: []const u8) f32 {
    var total: f32 = 0.0;
    var i: usize = 0;
    while (i < utf8.len) {
        var cp: u32 = 0;
        const n = utf8Decode(utf8[i..], &cp);
        if (n == 0) {
            i += 1;
            continue;
        }
        total += getCharWeight(cp);
        i += n;
    }
    return total;
}

/// Reference default arguments of `duration_estimate`.
pub const default_low_threshold: f32 = 50.0;
pub const default_boost_strength: f32 = 3.0;

/// Estimates the target duration in the same unit as `ref_duration`.
/// Identical formula to `RuleDurationEstimator.estimate_duration`, including
/// the power-curve boost when the linear estimate falls below
/// `low_threshold` (reference: `low_threshold * powf(est / low_threshold,
/// 1 / boost_strength)`).
pub fn estimate(target_text: []const u8, ref_text: []const u8, ref_duration: f32, low_threshold: f32, boost_strength: f32) f32 {
    if (ref_duration <= 0.0 or ref_text.len == 0) {
        return 0.0;
    }
    const ref_weight = totalWeight(ref_text);
    if (ref_weight == 0.0) {
        return 0.0;
    }
    const speed_factor = ref_weight / ref_duration;
    const target_weight = totalWeight(target_text);
    const estimated = target_weight / speed_factor;
    if (estimated < low_threshold) {
        const alpha = 1.0 / boost_strength;
        return low_threshold * std.math.pow(f32, estimated / low_threshold, alpha);
    }
    return estimated;
}

/// Number of target audio tokens for a synthesis call. Mirrors
/// `OmniVoice._estimate_target_tokens` via `duration_estimate_tokens`: when
/// no reference is available, falls back to the canonical
/// "Nice to meet you." anchor at 25 tokens. The estimate is truncated toward
/// zero (Python int() / C `(int)` cast) and floored to 1.
pub fn estimateTokens(target_text: []const u8, ref_text: []const u8, ref_audio_tokens: i32) i32 {
    var rt: []const u8 = undefined;
    var rd: i32 = undefined;
    if (ref_audio_tokens > 0 and ref_text.len != 0) {
        rt = ref_text;
        rd = ref_audio_tokens;
    } else {
        rt = "Nice to meet you.";
        rd = 25;
    }
    const est = estimate(target_text, rt, @floatFromInt(rd), default_low_threshold, default_boost_strength);
    var tokens: i32 = @intFromFloat(est);
    if (tokens < 1) {
        tokens = 1;
    }
    return tokens;
}

