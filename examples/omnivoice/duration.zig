//! Text duration estimation for the OmniVoice example. Port of
//! `refs/omnivoice.cpp/src/duration-estimator.h` (itself a byte-perfect
//! mirror of omnivoice/utils/duration.py `RuleDurationEstimator`).
//!
//! Returns an estimated audio duration (in the same unit as `ref_duration`,
//! typically audio tokens) for a target text given a reference text whose
//! duration is known. Uses per-script phonetic weights and Unicode category
//! fallbacks to handle 600+ scripts uniformly. All arithmetic is f32 with
//! the reference's accumulation order.

const std = @import("std");

/// Top-level Unicode category classes used by the estimator (reference
/// DUR_M/P/S/Z/N). Generated from CPython 15.1.0 UCD over [0, 0x110000).
pub const Cat = enum(u8) {
    m, // Mark        (weight 0.0)
    p, // Punctuation (weight 0.5)
    s, // Symbol      (weight 0.5)
    z, // Separator   (weight 0.2)
    n, // Number      (weight 3.5)
};

/// Script classes (reference DurationScript). The last four are the
/// category-derived classes.
pub const Script = enum(u8) {
    cjk,
    hangul,
    kana,
    ethiopic,
    yi,
    indic,
    thai_lao,
    khmer_myanmar,
    arabic,
    hebrew,
    latin,
    cyrillic,
    greek,
    armenian,
    georgian,
    default,
    // category-derived classes:
    punctuation,
    space,
    digit,
    mark,
};

/// Reference `duration_weights`, indexed by `@intFromEnum(Script)`.
pub const weights = [_]f32{
    3.0, // CJK
    2.5, // HANGUL
    2.2, // KANA
    3.0, // ETHIOPIC
    3.0, // YI
    1.8, // INDIC
    1.5, // THAI_LAO
    1.8, // KHMER_MYANMAR
    1.5, // ARABIC
    1.5, // HEBREW
    1.0, // LATIN
    1.0, // CYRILLIC
    1.0, // GREEK
    1.0, // ARMENIAN
    1.0, // GEORGIAN
    1.0, // DEFAULT
    0.5, // PUNCTUATION
    0.2, // SPACE
    3.5, // DIGIT
    0.0, // MARK
};

fn weightOf(script: Script) f32 {
    return weights[@intFromEnum(script)];
}

