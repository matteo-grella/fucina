fn lowerByte(c: u8) u8 {
    return if (c >= 'A' and c <= 'Z') c + 32 else c;
}

/// True iff the ASCII-lowercased `input` equals `target` byte-wise. Exactly
/// equivalent to the reference's lower-copy-then-compare because every table
/// name (and "none") is free of 'A'..'Z' bytes.
fn eqlLowered(input: []const u8, target: []const u8) bool {
    if (input.len != target.len) return false;
    for (input, target) |c, t| {
        if (lowerByte(c) != t) return false;
    }
    return true;
}

/// Port of `resolve_language`. The returned slice borrows either `language`
/// itself (ISO-id passthrough) or the static table; "" on empty/"none"/miss.
/// The table has no duplicate names, so this linear scan is equivalent to the
/// reference's hash-map lookup.
pub fn resolveLanguage(language: []const u8) []const u8 {
    if (language.len == 0) return "";
    if (eqlLowered(language, "none")) return "";

    // Pass-through if the ORIGINAL (case-sensitive) input is a valid ISO id.
    for (&lang_name_to_id_table) |*entry| {
        if (std.mem.eql(u8, language, entry[1])) return language;
    }

    // Name lookup on the ASCII-lowercased input.
    for (&lang_name_to_id_table) |*entry| {
        if (eqlLowered(language, entry[0])) return entry[1];
    }
    return "";
}

