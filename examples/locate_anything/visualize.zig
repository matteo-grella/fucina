pub fn renderBoxes(allocator: std.mem.Allocator, img: *const Image, boxes: []const Box) !Image {
    var out = Image{
        .allocator = allocator,
        .w = img.w,
        .h = img.h,
        .rgb = try allocator.dupe(u8, img.rgb),
    };
    for (boxes) |*box| drawBox(&out, box, 3);
    return out;
}
