fn labelColor(label: []const u8) [3]u8 {
    const idx: usize = @intCast(std.hash.Fnv1a_64.hash(label) % palette.len);
    return palette[idx];
}

fn setPx(img: *Image, x: isize, y: isize, color: [3]u8) void {
    if (x < 0 or y < 0 or x >= @as(isize, @intCast(img.w)) or y >= @as(isize, @intCast(img.h))) return;
    const i = (@as(usize, @intCast(y)) * img.w + @as(usize, @intCast(x))) * 3;
    img.rgb[i..][0..3].* = color;
}

fn drawHline(img: *Image, x1_in: isize, x2_in: isize, y: isize, color: [3]u8) void {
    const x1 = @min(x1_in, x2_in);
    const x2 = @max(x1_in, x2_in);
    var x = x1;
    while (x <= x2) : (x += 1) setPx(img, x, y, color);
}

fn drawVline(img: *Image, x: isize, y1_in: isize, y2_in: isize, color: [3]u8) void {
    const y1 = @min(y1_in, y2_in);
    const y2 = @max(y1_in, y2_in);
    var y = y1;
    while (y <= y2) : (y += 1) setPx(img, x, y, color);
}

fn fillRect(img: *Image, x1: isize, y1: isize, x2: isize, y2: isize, color: [3]u8) void {
    var y = @min(y1, y2);
    while (y <= @max(y1, y2)) : (y += 1) drawHline(img, x1, x2, y, color);
}

fn drawText(img: *Image, x: isize, y: isize, text: []const u8, color: [3]u8, scale: isize) void {
    var cx = x;
    for (text) |ch| {
        const glyph = font8x8_basic[if (ch >= 128) '?' else ch];
        for (glyph, 0..) |bits, row| {
            for (0..8) |col| {
                if (bits & (@as(u8, 1) << @intCast(col)) != 0) {
                    const px = cx + @as(isize, @intCast(col)) * scale;
                    const py = y + @as(isize, @intCast(row)) * scale;
                    var sy: isize = 0;
                    while (sy < scale) : (sy += 1) {
                        var sx: isize = 0;
                        while (sx < scale) : (sx += 1) setPx(img, px + sx, py + sy, color);
                    }
                }
            }
        }
        cx += 8 * scale;
    }
}

fn drawBox(img: *Image, box: *const Box, thickness: isize) void {
    if (img.w == 0 or img.h == 0) return;
    const color = labelColor(box.label);
    const w: isize = @intCast(img.w);
    const h: isize = @intCast(img.h);

    var x1: isize = @intFromFloat(@round(box.x1));
    var y1: isize = @intFromFloat(@round(box.y1));
    var x2: isize = @intFromFloat(@round(box.x2));
    var y2: isize = @intFromFloat(@round(box.y2));
    if (x2 < x1) std.mem.swap(isize, &x1, &x2);
    if (y2 < y1) std.mem.swap(isize, &y1, &y2);
    x1 = std.math.clamp(x1, 0, w - 1);
    y1 = std.math.clamp(y1, 0, h - 1);
    x2 = std.math.clamp(x2, 0, w - 1);
    y2 = std.math.clamp(y2, 0, h - 1);

    var stroke: isize = @max(thickness, 3);
    const max_stroke = @max(@min(@divTrunc(x2 - x1, 2), @divTrunc(y2 - y1, 2)), 1);
    stroke = @min(stroke, max_stroke);

    var t: isize = 0;
    while (t < stroke) : (t += 1) {
        drawHline(img, x1, x2, y1 + t, color);
        drawHline(img, x1, x2, y2 - t, color);
        drawVline(img, x1 + t, y1, y2, color);
        drawVline(img, x2 - t, y1, y2, color);
    }

    const name = if (box.label.len == 0) "?" else box.label;
    var scale: isize = 1;
    if (img.w >= 640) scale = 2;
    if (img.w >= 1280) scale = 3;

    const padding = 3 * scale;
    const label_w = @as(isize, @intCast(name.len)) * 8 * scale;
    const label_h = 8 * scale;
    const bg_w = label_w + 2 * padding;
    const bg_h = label_h + 2 * padding;

    const bx1 = x1;
    var by1 = y1 - bg_h;
    if (by1 < 0) by1 = y1 + stroke;
    const bx2 = @min(bx1 + bg_w - 1, w - 1);
    const by2 = @min(by1 + bg_h - 1, h - 1);
    fillRect(img, bx1, by1, bx2, by2, color);

    const luma = (299 * @as(u32, color[0]) + 587 * @as(u32, color[1]) + 114 * @as(u32, color[2])) / 1000;
    const text_color: [3]u8 = if (luma > 160) .{ 0, 0, 0 } else .{ 255, 255, 255 };
    drawText(img, bx1 + padding, by1 + padding, name, text_color, scale);
}

