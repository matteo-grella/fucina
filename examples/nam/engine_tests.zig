fn fillRenderParityInput(input: []f32) void {
    for (input, 0..) |*v, i| {
        const phase: i32 = @intCast(i % 17);
        var value = @as(f32, @floatFromInt(phase - 8)) / 32.0;
        switch (i) {
            3 => value += 0.25,
            64 => value -= 0.125,
            97 => value += 0.1875,
            else => {},
        }
        v.* = value;
    }
}

fn expectRenderGolden(model_bytes: []const u8, expected: *const [parity_input_len]f32) !void {
    const allocator = std.testing.allocator;
    var model = try nam_file.loadFromSlice(allocator, model_bytes);
    defer model.deinit();

    var engine = try Engine.init(allocator, &model);
    defer engine.deinit();
    try engine.reset(parity_chunk, true);

    var input: [parity_input_len]f32 = undefined;
    fillRenderParityInput(&input);
    var output: [parity_input_len]f32 = undefined;
    var offset: usize = 0;
    while (offset < input.len) {
        const n = @min(parity_chunk, input.len - offset);
        try engine.process(input[offset..][0..n], output[offset..][0..n], n);
        offset += n;
    }

    for (expected.*, output) |want, got| {
        try std.testing.expectApproxEqAbs(want, got, 5e-6);
    }
}

test "engine loads and runs every supported vendored model" {
    const allocator = std.testing.allocator;
    const files = [_][]const u8{
        @embedFile("testdata/wavenet.nam"),
        @embedFile("testdata/lstm.nam"),
    };
    for (files) |bytes| {
        var model = try nam_file.loadFromSlice(allocator, bytes);
        defer model.deinit();
        var engine = try Engine.init(allocator, &model);
        defer engine.deinit();

        try engine.reset(64, true);
        var input: [64]f32 = undefined;
        for (&input, 0..) |*v, i| v.* = 0.1 * @sin(@as(f32, @floatFromInt(i)) * 0.3);
        var output: [64]f32 = undefined;
        try engine.process(&input, &output, 64);
        for (output) |v| try std.testing.expect(std.math.isFinite(v));

        // frames > max buffer is a checked error (deviation 3).
        var big_in: [128]f32 = undefined;
        @memset(&big_in, 0);
        var big_out: [128]f32 = undefined;
        try std.testing.expectError(Error.FramesExceedMaxBuffer, engine.process(&big_in, &big_out, 128));
    }
}

